// Number
let number = 5;
let factorial = 1;

// Calculate factorial
for (let i = 1; i <= number; i++) {
  factorial = factorial * i;
}

// Alert
alert("Factorial of " + number + " is: " + factorial);

// Console
console.log("Factorial of", number, "is:", factorial);

// Document
document.write("Factorial of " + number + " is: " + factorial);
